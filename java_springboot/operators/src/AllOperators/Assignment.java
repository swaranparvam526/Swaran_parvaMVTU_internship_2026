package AllOperators;

public class Assignment {
	public static void main(String[] args) {
		int a = 8;

        a += 9;
        System.out.println("a += 4 : " + a);

        a -= 2;
        System.out.println("a -= 2 : " + a);

        a *= 3;
        System.out.println("a *= 3 : " + a);

        a /= 6;
        System.out.println("a /= 2 : " + a);

        a %= 4;
        System.out.println("a %= 5 : " + a);
	}
}

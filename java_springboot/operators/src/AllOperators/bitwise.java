package AllOperators;

public class bitwise {
	public static void main(String[] args) {
		int a = 6;
        int b = 2;

        System.out.println("a & b : " + (a & b));
        System.out.println("a | b : " + (a | b));
        System.out.println("a ^ b : " + (a ^ b));
        System.out.println("~a : " + ~a);
	}
}
